package JavaOperators;

public class ArithmeticOperators {
    public static void main(String[] args) {
        int a=5;
        System.out.println(5 + 5 ); // +	Addition	Adds together two values
        System.out.println(5 - 5); //  -  Subtraction	 Subtracts one value from another
        System.out.println(5 * 5 );//  *	Multiplication	Multiplies two values
        System.out.println(5 / 5);//   /	Division	Divides one value by another
        System.out.println(5 % 5);//   %	Modulus		Returns the division remainder
        System.out.println((a++)); //  ++	Increment	Increases the value of a variable by 1
        System.out.println(a--); // --	Decrement	Decreases the value of a variable by 1
    }
}
